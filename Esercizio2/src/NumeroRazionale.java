
public class NumeroRazionale extends Numero{

	public NumeroRazionale(int numeratore, int denominatore) {
		super(numeratore, denominatore);
		
	}
	
	@Override
	public int getNumeratore() {
		return super.getNumeratore();
	}

	@Override
	public void setNumeratore(int numeratore) {
		super.setNumeratore(numeratore);
	}

	@Override
	public int getDenominatore() {
		return super.getDenominatore();
	}

	@Override
	public void setDenominatore(int denominatore) {
		super.setDenominatore(denominatore);
	}

	 
	 
}
